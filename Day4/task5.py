"""
m = [
[10,20,30],
[40,50,60],
[70,80,90]
]

Replace 60 with 6
[10, 20, 30] [40, 50, 6]
[50, 6] [80, 90]
[10, 50, 90]
[30, 50, 70]
[20, 50, 80]
[40, 60]
[10, 70]
[20, 30] [80, 90]
[40, 60] [70, 90]
"""

m = [
    [10, 20, 30],
    [40, 50, 60],
    [70, 80, 90]
]

# Replace 60 with 6
m[1][2] = 6
print(m)

# [10, 20, 30] [40, 50, 6]
print(m[0], m[1])

# [50, 6] [80, 90]
print(m[1][1:], m[2][1:])

# [10, 50, 90]
print([m[i][i] for i in range(len(m))])

# [30, 50, 70]
print([m[i][2-i] for i in range(len(m))])

# [20, 50, 80]
print([row[1] for row in m])

# [40, 60]
print([m[1][0], m[1][2]])

# [10, 70]
print([m[0][0], m[2][0]])

# [20, 30] [80, 90]
print(m[0][1:], m[2][1:])

# [40, 60] [70, 90]
print([m[1][0], m[1][2]], [m[2][0], m[2][2]])
