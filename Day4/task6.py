A = [ 
    [11, 0, 4, 6, 5], 
    [1, 9, 3, 2, 1], 
    [7, 0, 4, 9, 8], 
    [3, 7, 12, 15, 0] 
]

# 1) 12
print(A[3][2])

# 2) [9,3,2] [0,4,9]
print(A[1][1:4], A[2][1:4])

# 3) [9,0]
print([A[1][1], A[2][1]])

# 4) [1,9,3] [7,0,4] [3,7,12]
print(A[1][:3], A[2][:3], A[3][:3])

# 5) [11,4,5] [3,12,0]
print([A[0][0], A[0][2], A[0][4]], [A[3][0], A[3][2], A[3][4]])

# 6) Add [0,0,0,0,0] after [3,7,12,15,0]
A.insert(4, [0, 0, 0, 0, 0])
print(A)

# 7) [11,9,4,15,0]
print([A[i][i] for i in range(len(A)-1)])

# 8) [5,2,4,7,0]
print([A[i][4] for i in range(len(A)-1)])

# 9) [1,9,3,2,1] [7,0,4,9,8]
print(A[1], A[2])

# 10) [4,6] [3,2] [4,9] [12,15]
print(A[0][2:4], A[1][2:4], A[2][2:4], A[3][2:4])
