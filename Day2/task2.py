"""
"Python String Slicing Example"
1) gnirtS nohtyP
2) Slicing Example
3) emEni iS oy
4) Potgigae
5) elpmaxE
6) gtoP
"""

s= "Python String Slicing Example"

# gnirtS nohtyP
print(s[12::-1])
print(s[-17::-1])

# Slicing Example
print(s[14::1])
print(s[-15::1])

# emEni iS oy
print(s[28:0:-3])
print(s[-1:-29:-3])

# Potgigae
print(s[0::4])
print(s[-29::4])

# elpmaxE
print(s[28:21:-1])
print(s[-1:-8:-1])

# gtoP
print(s[12::-4])
print(s[-17::-4])
